void MarchingCubes::loadScalarField(const btFluidSph& F, int cellsPerEdge, 
									btAlignedObjectArray<btScalar>* out_scalarField, btVector3* out_cellSize)
{	
	btVector3 aabbMin = F.getLocalParameters().m_aabbBoundaryMin;		
	btVector3 aabbMax = F.getLocalParameters().m_aabbBoundaryMax;
	
	//Fluid volume(aabbMin, aabbMax) is not necessarily cube-shaped; force a cube-shaped grid
	const bool FORCE_CUBE_SHAPED_GRID = false;
	if(FORCE_CUBE_SHAPED_GRID)
	{
		btScalar highestScalar = 0.f;
		if( btFabs(aabbMin.x()) > highestScalar ) highestScalar = btFabs( aabbMin.x() );
		if( btFabs(aabbMin.y()) > highestScalar ) highestScalar = btFabs( aabbMin.y() );
		if( btFabs(aabbMin.z()) > highestScalar ) highestScalar = btFabs( aabbMin.z() );
		if( btFabs(aabbMax.x()) > highestScalar ) highestScalar = btFabs( aabbMax.x() );
		if( btFabs(aabbMax.y()) > highestScalar ) highestScalar = btFabs( aabbMax.y() );
		if( btFabs(aabbMax.z()) > highestScalar ) highestScalar = btFabs( aabbMax.z() );
		
		aabbMin.setValue(-highestScalar, -highestScalar, -highestScalar);
		aabbMax.setValue(highestScalar, highestScalar, highestScalar);
	}
	
	//Fill out_scalarField
	const int cellsPerPlane = cellsPerEdge * cellsPerEdge;
	const bool USE_MARGIN = true;
	if(!USE_MARGIN)
	{
		btVector3 cellSize( aabbMax.x() - aabbMin.x(), aabbMax.y() - aabbMin.y(), aabbMax.z() - aabbMin.z() );
		cellSize /= static_cast<btScalar>(cellsPerEdge);
		
		for(int z = 0; z < cellsPerEdge; ++z)
		for(int y = 0; y < cellsPerEdge; ++y)
		for(int x = 0; x < cellsPerEdge; ++x)
		{
			btVector3 position = getVertex(aabbMin, cellSize, x, y, z);
		
			(*out_scalarField)[x + y*cellsPerEdge + z*cellsPerPlane] = F.getValue( position.x(), position.y(), position.z() );
		}
		
		*out_cellSize = cellSize;
	}
	else
	{
		//Since marching cubes does not generate any vertices if a cube is fully 
		//above or below the isolevel, the fluid particles in the the outermost
		//cells may not be rendered. To resolve this, include a margin so that
		//there is always a boundary between filled and empty cells.
	
		//AABB is within margins (for a 16^3 grid, 14^3 cells are in the AABB)
		btVector3 cellSize( aabbMax.x() - aabbMin.x(), aabbMax.y() - aabbMin.y(), aabbMax.z() - aabbMin.z() );
		cellSize /= static_cast<btScalar>(cellsPerEdge - 2);	//Subtract 2 to exclude margins
		
		btVector3 adjusted_min = aabbMin;
		adjusted_min -= cellSize;	//-1 to get the 'actual' grid min, rather than the one contained in the margins
		
		//Excluding the outermost cells, fill out_scalarField
		for(int z = 1; z < cellsPerEdge-1; ++z)
		for(int y = 1; y < cellsPerEdge-1; ++y)
		for(int x = 1; x < cellsPerEdge-1; ++x)
		{
			btVector3 position = getVertex(adjusted_min, cellSize, x, y, z);
			
			(*out_scalarField)[x + y*cellsPerEdge + z*cellsPerPlane] = F.getValue( position.x(), position.y(), position.z() );
		}
		
		*out_cellSize = cellSize;
	}
}
void MarchingCubes::marchingCubes(const btVector3& aabbMin, const btVector3& cellSize, const btAlignedObjectArray<btScalar>& scalarField, 
								  int cellsPerEdge, btAlignedObjectArray<float>* out_vertices)
{	
	//Amount to add to get the next element in that dimension (for scalarField indices)
	//const int stride_x = 1;								//Left-Right
	const int stride_y = cellsPerEdge;						//Up-Down
	const int stride_z = cellsPerEdge * cellsPerEdge;		//Forward-Back
	
	//Generate and render marching_cubes
	for(int z = 0; z < cellsPerEdge-1; ++z)
	for(int y = 0; y < cellsPerEdge-1; ++y)
	for(int x = 0; x < cellsPerEdge-1; ++x)
	{
		MarchingCube C;
		
		//Lower square	
		int current_x = x;
		int current_y = y;
		int current_z = z;
			C.m_vertices[0] = getVertex(aabbMin, cellSize, current_x , current_y, current_z);
			C.m_scalars[0] = scalarField[ current_x + (current_y*stride_y) + (current_z*stride_z) ];
		
		current_x = x + 1;
		current_y = y;
		current_z = z;
			C.m_vertices[1] = getVertex(aabbMin, cellSize, current_x , current_y, current_z);
			C.m_scalars[1] = scalarField[ current_x + (current_y*stride_y) + (current_z*stride_z) ];
			
		current_x = x + 1;
		current_y = y;
		current_z = z + 1;		
			C.m_vertices[2] = getVertex(aabbMin, cellSize, current_x , current_y, current_z);
			C.m_scalars[2] = scalarField[ current_x + (current_y*stride_y) + (current_z*stride_z) ];
			
		current_x = x;
		current_y = y;
		current_z = z + 1;	
			C.m_vertices[3] = getVertex(aabbMin, cellSize, current_x , current_y, current_z);
			C.m_scalars[3] = scalarField[ current_x + (current_y*stride_y) + (current_z*stride_z) ];
		
		//Upper square (same as lower square, except 'current_y = y + 1')
		current_x = x;
		current_y = y + 1;
		current_z = z;
			C.m_vertices[4] = getVertex(aabbMin, cellSize, current_x , current_y, current_z);
			C.m_scalars[4] = scalarField[ current_x + (current_y*stride_y) + (current_z*stride_z) ];
		
		current_x = x + 1;
		current_y = y + 1;
		current_z = z;	
			C.m_vertices[5] = getVertex(aabbMin, cellSize, current_x , current_y, current_z);
			C.m_scalars[5] = scalarField[ current_x + (current_y*stride_y) + (current_z*stride_z) ];
			
		current_x = x + 1;
		current_y = y + 1;
		current_z = z + 1;
			C.m_vertices[6] = getVertex(aabbMin, cellSize, current_x , current_y, current_z);
			C.m_scalars[6] = scalarField[ current_x + (current_y*stride_y) + (current_z*stride_z) ];
			
		current_x = x;
		current_y = y + 1;
		current_z = z + 1;	
			C.m_vertices[7] = getVertex(aabbMin, cellSize, current_x , current_y, current_z);
			C.m_scalars[7] = scalarField[ current_x + (current_y*stride_y) + (current_z*stride_z) ];
			
		generateVertices(C, out_vertices);
	}
}					  
void MarchingCubes::generateVertices(const MarchingCube& C, btAlignedObjectArray<float>* out_vertices)
{
	const btScalar ISOLEVEL = 1.0f;
	
	//Compare scalars at vertices 0-7 with ISOLEVEL 
	//to determine which of the 256 possible configurations is present
	unsigned char cubeIndex = 0;
	if( C.m_scalars[0] < ISOLEVEL ) cubeIndex |= 1;
	if( C.m_scalars[1] < ISOLEVEL ) cubeIndex |= 2;
	if( C.m_scalars[2] < ISOLEVEL ) cubeIndex |= 4;
	if( C.m_scalars[3] < ISOLEVEL ) cubeIndex |= 8;
	if( C.m_scalars[4] < ISOLEVEL ) cubeIndex |= 16;
	if( C.m_scalars[5] < ISOLEVEL ) cubeIndex |= 32;
	if( C.m_scalars[6] < ISOLEVEL ) cubeIndex |= 64;
	if( C.m_scalars[7] < ISOLEVEL ) cubeIndex |= 128;
	
	if( !EDGE_TABLE[cubeIndex] ) return;		//All vertices are above or below ISOLEVEL
	
	//Generate vertices for edges 0-11; interpolate between the edge's vertices
	btVector3 vertices[12];
	if( EDGE_TABLE[cubeIndex] & 1    ) vertices[0]  = vertexLerp(ISOLEVEL, C.m_scalars[0], C.m_scalars[1], C.m_vertices[0], C.m_vertices[1]);
	if( EDGE_TABLE[cubeIndex] & 2    ) vertices[1]  = vertexLerp(ISOLEVEL, C.m_scalars[1], C.m_scalars[2], C.m_vertices[1], C.m_vertices[2]);
	if( EDGE_TABLE[cubeIndex] & 4    ) vertices[2]  = vertexLerp(ISOLEVEL, C.m_scalars[2], C.m_scalars[3], C.m_vertices[2], C.m_vertices[3]);
	if( EDGE_TABLE[cubeIndex] & 8    ) vertices[3]  = vertexLerp(ISOLEVEL, C.m_scalars[3], C.m_scalars[0], C.m_vertices[3], C.m_vertices[0]);
	if( EDGE_TABLE[cubeIndex] & 16   ) vertices[4]  = vertexLerp(ISOLEVEL, C.m_scalars[4], C.m_scalars[5], C.m_vertices[4], C.m_vertices[5]);
	if( EDGE_TABLE[cubeIndex] & 32   ) vertices[5]  = vertexLerp(ISOLEVEL, C.m_scalars[5], C.m_scalars[6], C.m_vertices[5], C.m_vertices[6]);
	if( EDGE_TABLE[cubeIndex] & 64   ) vertices[6]  = vertexLerp(ISOLEVEL, C.m_scalars[6], C.m_scalars[7], C.m_vertices[6], C.m_vertices[7]);
	if( EDGE_TABLE[cubeIndex] & 128  ) vertices[7]  = vertexLerp(ISOLEVEL, C.m_scalars[7], C.m_scalars[4], C.m_vertices[7], C.m_vertices[4]);
	if( EDGE_TABLE[cubeIndex] & 256  ) vertices[8]  = vertexLerp(ISOLEVEL, C.m_scalars[0], C.m_scalars[4], C.m_vertices[0], C.m_vertices[4]);
	if( EDGE_TABLE[cubeIndex] & 512  ) vertices[9]  = vertexLerp(ISOLEVEL, C.m_scalars[1], C.m_scalars[5], C.m_vertices[1], C.m_vertices[5]);
	if( EDGE_TABLE[cubeIndex] & 1024 ) vertices[10] = vertexLerp(ISOLEVEL, C.m_scalars[2], C.m_scalars[6], C.m_vertices[2], C.m_vertices[6]);
	if( EDGE_TABLE[cubeIndex] & 2048 ) vertices[11] = vertexLerp(ISOLEVEL, C.m_scalars[3], C.m_scalars[7], C.m_vertices[3], C.m_vertices[7]);

	//Store triangles
	for(int i = 0; i < 16 && TRIANGLE_TABLE[cubeIndex][i] != -1; ++i) 
	{
		const btVector3& V = vertices[ TRIANGLE_TABLE[cubeIndex][i] ];
		out_vertices->push_back( static_cast<float>( V.x() ) );
		out_vertices->push_back( static_cast<float>( V.y() ) );
		out_vertices->push_back( static_cast<float>( V.z() ) );
	}
}



