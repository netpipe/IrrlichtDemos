f32 MetaballSceneNode::calculateValue(vector3df const& position) {
	f32 value = 0;
	const u32 ballSize =  Balls.size();
	for (u32 i = 0; i < ballSize; i++)
		if (Balls[i] != position)
			value += valueFunction(position, Balls[i]);
	return value;
}

SColor MetaballSceneNode::calculateColor(vector3df const& position) {
	core::array<f32> value;
	value.reallocate(Balls.size());
	f32 total = 0;
	f32 max = 0;
	for (u32 i = 0; i < Balls.size(); i++) {
		const f32 currentValue = valueFunction(position, Balls[i]);
		value.push_back(currentValue);
		total += currentValue;
	}

	vector3di c(0, 0, 0);
	f32 preDivTotal = 1.0f / total;
	for (u32 i = 0; i < value.size(); i++) {
		c.X += value[i] * preDivTotal * Colors[i].getRed();
		c.Y += value[i] * preDivTotal * Colors[i].getGreen();
		c.Z += value[i] * preDivTotal * Colors[i].getBlue();
	}

	return SColor(255, c.X, c.Y, c.Z);
}


